package pruebastringtokenize;

/**
 *
 * @author Adrian
 */
public class DetalleDocumento {
    
    private long posicionB;
    private int lineas;
    private int path;

    /**
     * @return the posicionB
     */
    public long getPosicionB() {
        return posicionB;
    }

    /**
     * @param posicionB the posicionB to set
     */
    public void setPosicionB(long posicionB) {
        this.posicionB = posicionB;
    }

    /**
     * @return the lineas
     */
    public int getLineas() {
        return lineas;
    }

    /**
     * @param lineas the lineas to set
     */
    public void setLineas(int lineas) {
        this.lineas = lineas;
    }

    /**
     * @return the path
     */
    public int getPath() {
        return path;
    }

    /**
     * @param path the path to set
     */
    public void setPath(int path) {
        this.path = path;
    }
    
}
